#include "libft.h"

int	ft_atoi(const char *str)
{
	long	i;
	int		s;
	long	res;

	res = 0;
	i = 0;
	s = 1;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n' || str[i] == '\v'
		|| str[i] == '\f' || str[i] == '\r')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			s *= -1;
		i++;
	}
	while ((str[i] - '0') >= 0 && (str[i] - '0') <= 9)
	{
		res = (10 * res) + (str[i] - '0');
		i++;
	}
	return (res * s);
}
